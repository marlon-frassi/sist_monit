void HAL_ADC_ConvCpltCallback(ADC_HandleTypeDef* hadc) 	// DO WHAT YOU WANT ON THE ADC
{
	if(flag == 0)
	{
		if( i < (NSample))
		{
			if( i >= NStart) //  Start buffer recorder
				{
				//	Buffer
				V1_Data[i] = V1n = (((adc_array[0] * 0.0008056640625) - 1.6453255531) * 1095.36);
				V2_Data[i] = V2n = (((adc_array[1] * 0.0008056640625) - 1.6399012802) * 1095.36);
				V3_Data[i] = V3n = (((adc_array[2] * 0.0008056640625) - 1.6440070487) * 1095.36);

				SumV1 = SumV1 + V1n*V1n;
				SumV2 = SumV2 + V2n*V2n;
				SumV3 = SumV3 + V3n*V3n;
				SumTemp = SumTemp + ((adc_array[3] * 0.0008056640625) * 100);

				// Restart Acquisition
				HAL_ADCEx_MultiModeStart_DMA(&hadc1, (uint32_t*)adc_array, sizeof(adc_array));
				}
			else
				{
				// Restart Acquisition
				HAL_ADCEx_MultiModeStart_DMA(&hadc1, (uint32_t*)adc_array, sizeof(adc_array));
				}
		}
		else
		{
			// Calculation of the RMS values and reset tags and variables
			V1rms = sqrt (SumV1/NSampleStart);
			V2rms = sqrt (SumV2/NSampleStart);
			V3rms = sqrt (SumV3/NSampleStart);
			Temperatura = (SumTemp/NSampleStart);
			SumV1 = 0, SumV2 = 0, SumV3 = 0;
			SumTemp = 0;
			i = 0;
			flag = 1;

		}
	i++;
	}
}
